import type { QuizQuestion, TrainingTrackModule, TrainingTrackVideo, TrackStageId } from "@/types/training";

function q(moduleId: string, n: number, text: string, options: [string, string, string], correctIdx: 0 | 1 | 2): QuizQuestion {
  return {
    id: `${moduleId}-q${n}`,
    text,
    type: "single",
    sortOrder: n,
    answers: options.map((label, i) => ({
      id: `${moduleId}-q${n}-a${i + 1}`,
      text: label,
      isCorrect: i === correctIdx
    }))
  };
}

function moduleVideos(moduleId: string): TrainingTrackVideo[] {
  return [1, 2, 3, 4].map((n) => ({
    id: `${moduleId}-video-${n}`,
    title: `Видео ${n}`,
    goal: "Цель этого урока будет добавлена позже.",
    sortOrder: n
  }));
}

function crmModule(
  id: string,
  title: string,
  shortDescription: string,
  sortOrder: number,
  sections: { title: string; content: string }[],
  questions: QuizQuestion[]
): TrainingTrackModule {
  return {
    id,
    stageId: "crm",
    title,
    shortDescription,
    passingScore: 80,
    sortOrder,
    sections: sections.map((section, index) => ({
      id: `${id}-s${index + 1}`,
      title: section.title,
      content: section.content,
      sortOrder: index + 1
    })),
    videos: moduleVideos(id),
    questions
  };
}

function practiceModule(
  id: string,
  title: string,
  shortDescription: string,
  sortOrder: number,
  sections: { title: string; content: string }[],
  questions: QuizQuestion[]
): TrainingTrackModule {
  return {
    id,
    stageId: "practice",
    title,
    shortDescription,
    passingScore: 80,
    sortOrder,
    sections: sections.map((section, index) => ({
      id: `${id}-s${index + 1}`,
      title: section.title,
      content: section.content,
      sortOrder: index + 1
    })),
    questions
  };
}

