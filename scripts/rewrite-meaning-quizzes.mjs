/** @param {string} id @param {number} n @param {string} text @param {[string,string,string]} options @param {0|1|2} correctIdx */
function q(id, n, text, options, correctIdx) {
  return {
    id: `${id}-q${n}`,
    text,
    type: "single",
    sortOrder: n,
    answers: options.map((text, i) => ({
      id: `${id}-q${n}-a${i + 1}`,
      text,
      isCorrect: i === correctIdx
    }))
  };
}

